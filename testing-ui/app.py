# Cleanup function to remove upload files on exit
def cleanup_upload_files():
    """Remove upload files from ui/uploads directory."""
    try:
        if UPLOAD_FOLDER.exists():
            deleted_count = 0
            for file in UPLOAD_FOLDER.iterdir():
                try:
                    if file.is_file():
                        # Remove ALL files (they should be cleaned up immediately after processing)
                        file.unlink()
                        deleted_count += 1
                        print(f"Cleaned up upload file: {file.name}")
                except Exception as e:
                    print(f"Error cleaning up {file}: {e}")
            if deleted_count > 0:
                print(f"Cleaned up {deleted_count} upload file(s) from {UPLOAD_FOLDER}")
    except Exception as e:
        print(f"Error in cleanup_upload_files: {e}")

atexit.register(cleanup_upload_files)

